/* ---------- Badge ---------- */
function Badge({ estado }) {
  const cls = window.DATA.BADGE_CLASS[estado] || "inactivo";
  return <span className={`badge ${cls}`}><span className="dot"></span>{estado}</span>;
}

/* ---------- Field wrapper ---------- */
function Field({ label, required, locked, error, hint, children, full }) {
  return (
    <div className={`field${error ? " error" : ""}${full ? " col-2" : ""}`}>
      {label && (
        <label>
          {label}
          {required && <span className="req">*</span>}
          {locked && <Ico.lock className="lock-ico" />}
        </label>
      )}
      {children}
      {error && <div className="field-err"><Ico.alert width="13" height="13" />{error}</div>}
      {!error && hint && <div className="field-hint">{hint}</div>}
    </div>
  );
}

/* ---------- Read-only value (Ver screen) ---------- */
function ROValue({ value }) {
  const empty = value === undefined || value === null || value === "";
  return <div className={`ro-value${empty ? " empty" : ""}`}>{empty ? "No registro" : value}</div>;
}

/* ---------- Checkbox ---------- */
function Checkbox({ checked, onChange, label, disabled }) {
  return (
    <label className="chk" style={disabled ? { opacity: 0.55, cursor: "not-allowed" } : null}>
      <input type="checkbox" checked={checked} disabled={disabled}
        onChange={(e) => !disabled && onChange(e.target.checked)} />
      <span className="box"><Ico.check /></span>
      <span className="lbl">{label}</span>
    </label>
  );
}

/* ---------- Toast stack ---------- */
function Toasts({ items, onDismiss }) {
  return (
    <div className="toast-wrap">
      {items.map((t) => (
        <div key={t.id} className={`toast ${t.kind}`}>
          <span className="t-ico">{t.kind === "success" ? <Ico.checkCircle width="20" height="20" /> : <Ico.alert width="20" height="20" />}</span>
          <div style={{ flex: 1 }}>
            <div className="t-title">{t.title}</div>
            {t.msg && <div className="t-msg">{t.msg}</div>}
          </div>
          <button onClick={() => onDismiss(t.id)} style={{ border: "none", background: "none", color: "var(--muted)", padding: 0 }}><Ico.x width="16" height="16" /></button>
        </div>
      ))}
    </div>
  );
}

/* ---------- Card ---------- */
function Card({ num, title, action, children, noBody }) {
  return (
    <section className="card">
      {title && (
        <div className="card-head">
          {num && <span className="num">{num}</span>}
          <h3>{title}</h3>
          {action && <div style={{ marginLeft: "auto" }}>{action}</div>}
        </div>
      )}
      {noBody ? children : <div className="card-body">{children}</div>}
    </section>
  );
}

